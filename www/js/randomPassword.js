function rndRange(from, to) {

    // Returns a random integer between `from` and `to` (inclusive). Uses
    // window.crypto if available, otherwise falls back on Math.random()

    var range, mask, r, crypto;
    from = Math.floor(from);
    to = Math.floor(to);
    if (from > to) {
        var tmp = from;
        from = to;
        to = tmp;
    }
    range = to - from;
    if (range == 0) return from;
    if (range >= 0x100000000) RangeError("Range too large in rndRange()");
    for (mask = 0, r = range; r; r>>= 1) mask |= r;

    crypto = window.crypto || window.msCrypto;
    if (!crypto) return Math.floor(Math.random() * (range+1)) + from;

    var rval = new Int32Array(1);
    do {
        rval = crypto.getRandomValues(rval);
        rval[0] &= mask;
    } while (rval[0] > range);
    return rval[0];
}


function randomPassword(numChars) {

    // Generates a completely random password that could be used in
    // conjunction with a password manager, but will be difficult
    // for a human to remember.

    // Use a restricted alphabet to avoid spelling out rude messages
    // like "dr0p-d34d" (i.e., no vowels [aeiouy], no numerals that
    // look like vowels [0134]). Lower case "L" also excluded because
    // it is easily confused with "1".

    // With an alphabet of 25 characters, a 12-character password
    // has about 56 bits of entropy.

    var alphabet = "256789bcdfghjkmnpqrstvwxz";
    var i, pw = "";
    for (i=0; i<numChars; i++) {
        pw += alphabet.charAt(rndRange(0, alphabet.length-1));
        // Add hyphen after every 4 chars if numChars is a multiple of 4
        if (numChars%4 == 0 && i%4 == 3 && i<numChars-1) pw += '-';
    }
    return pw;
}


function strongPassword() {

    // Generates a password with >256 bits of entropy
    // Using an alphabet of 68 characters, this requires a password
    // length of 42 characters

    var alphabet = 'bcdfghjklmnpqrstvwxzBCDFGHJKLMNPQRSTVWXZ256789#$%^*()-_=+[{]};:|,./?';
    var i, pw = "";
    for (i=0; i<42; i++) pw += alphabet.charAt(rndRange(0, alphabet.length-1));
    return pw;
}


